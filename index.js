import { readCsv, writeCsv } from './libs/csv.js';
import { listOrderCosts, listCustomerRanking, listCustomersByProduct } from './libs/process.js';

const customers = await readCsv('./files/customers.csv');
const rawProducts = await readCsv('./files/products.csv');
const rawOrders = await readCsv('./files/orders.csv');

// Convert cost to Number
const products = rawProducts.map((product) => ({ ...product, cost: parseFloat(product.cost) }));

// Convert space-separated product ids to array of product ids
const orders = rawOrders.map((order) => ({
  ...order,
  products: order.products.split(/\s+/),
}));

// Task 1: List the cost of each order
writeCsv('./files/order_prices.csv', listOrderCosts({ orders, products }));

// Task 2: Customers that have purchased each product
writeCsv('./files/product_customers.csv', listCustomersByProduct({ orders, products }));

// Task 3: Ranking of customers by spending
writeCsv('./files/customer_ranking.csv', listCustomerRanking({ customers, orders, products }));

